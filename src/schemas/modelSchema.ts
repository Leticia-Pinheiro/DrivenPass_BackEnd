import joi from "joi"

const passwordRegex = /^\d{4}$/
const numberRegex = /^\d{4} \d{4} \d{4} \d{4}$/
const urlLinkRegex = /^(?:(?:https?|ftp):\/\/)(?:\S+(?::\S*)?@)?(?:(?!(?:10|127)(?:\.\d{1,3}){3})(?!(?:169\.254|192\.168)(?:\.\d{1,3}){2})(?!172\.(?:1[6-9]|2\d|3[0-1])(?:\.\d{1,3}){2})(?:[1-9]\d?|1\d\d|2[01]\d|22[0-3])(?:\.(?:1?\d{1,2}|2[0-4]\d|25[0-5])){2}(?:\.(?:[1-9]\d?|1\d\d|2[0-4]\d|25[0-4]))|(?:(?:[a-z\u00a1-\uffff0-9]-*)*[a-z\u00a1-\uffff0-9]+)(?:\.(?:[a-z\u00a1-\uffff0-9]-*)*[a-z\u00a1-\uffff0-9]+)*(?:\.(?:[a-z\u00a1-\uffff]{2,}))\.?)(?::\d{2,5})?(?:[/?#]\S*)?$/i
const urlFotoRegex = /(ftp|http|https):\/\/(\w+:{0,1}\w*@)?(\S+)(:[0-9]+)?(\/|\/([\w#!:.?+=&%@!\-\/]))?/

const modelSchema = joi.object({
	number: joi.string().pattern(numberRegex).required(),	
	securityCode: joi.string().length(3).required(),
	password: joi.string().length(4).pattern(passwordRegex).required(),
    urlLink: joi.string().pattern(urlLinkRegex).required(),
    urlFoto: joi.string().pattern(urlFotoRegex).required()
});

export default modelSchema;